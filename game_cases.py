from database.models import Case, Step, Option, Difficulty
from sqlalchemy import select

async def seed_game_cases(session):
    added = 0
    for case_data in GAME_CASES:
        result = await session.execute(select(Case).where(Case.title == case_data["title"]))
        if result.scalar_one_or_none():
            continue
        case = Case(title=case_data["title"], description=case_data["description"],
                    preamble=case_data["preamble"], industry=case_data.get("industry", ""),
                    difficulty=Difficulty(case_data["difficulty"]))
        session.add(case)
        await session.flush()
        for sdata in case_data["steps"]:
            step = Step(case_id=case.id, order=sdata["order"], title=sdata.get("title", ""),
                        data_snapshot=sdata.get("data_snapshot"), question=sdata["question"])
            session.add(step)
            await session.flush()
            for odata in sdata["options"]:
                session.add(Option(step_id=step.id, text=odata["text"],
                    is_correct=odata["is_correct"], explanation=odata.get("explanation", ""),
                    effects=odata.get("effects")))
        added += 1
    await session.commit()
    return added
