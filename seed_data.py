async def seed_case(db_session):
    """Загружает демо-кейс в БД (вызов из main)."""
    from database.models import Case, Step, Option, Difficulty
    from sqlalchemy import select

    result = await db_session.execute(select(Case).where(Case.title == CASE_JSON["title"]))
    if result.scalar_one_or_none():
        return

    case = Case(
        title=CASE_JSON["title"],
        description=CASE_JSON["description"],
        preamble=CASE_JSON["preamble"],
        industry=CASE_JSON.get("industry", ""),
        difficulty=Difficulty(CASE_JSON["difficulty"]),
    )
    db_session.add(case)
    await db_session.flush()

    for sdata in CASE_JSON["steps"]:
        step = Step(
            case_id=case.id,
            order=sdata["order"],
            title=sdata.get("title", ""),
            data_snapshot=sdata.get("data_snapshot"),
            question=sdata["question"],
        )
        db_session.add(step)
        await db_session.flush()

        for odata in sdata["options"]:
            opt = Option(
                step_id=step.id,
                text=odata["text"],
                is_correct=odata["is_correct"],
                explanation=odata.get("explanation", ""),
                effects=odata.get("effects"),
            )
            db_session.add(opt)

    await db_session.commit()


if __name__ == "__main__":
    import asyncio
    from database.db import async_session, init_db

    async def run():
        await init_db()
        async with async_session() as session:
            await seed_case(session)
            print("✅ Демо-кейс загружен!")

    asyncio.run(run())
